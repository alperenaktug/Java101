package privateClass;

public class MathClass {
    public static void main(String[] args) {
        System.out.println(Math.abs(-100));//mutalak değer
        System.out.println(Math.ceil(3.6));//en yakın tam asayıya yuvarlama
        System.out.println(Math.max(2,5)); // max değeri verir
        System.out.println(Math.min(2,5)); // min değeri verir
        System.out.println(Math.random()); // 0 , 1 arasında rastgele sayı verir
        System.out.println(Math.pow(2,3)); // üs alma işlemi yapar
        System.out.println(Math.sqrt(16)); // karakökünü alır

    }
}
